package SECCION_4;

public class Concatenar {

  public static void main(String[] args) {
    String nombre = "Codi";
    String curso = "Curso de Java 9";

    // Se puede concatenar con el operador +
    String mensaje = "Hola " + nombre + " bienvenido al " + curso + "!";

    System.out.println(mensaje);
  }
}
